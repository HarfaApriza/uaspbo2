    private void bUbahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bUbahActionPerformed
        // TODO add your handling code here:
        int i = tBuku.getSelectedRow();
        if (i>=0){
            TableModel model = tBuku.getModel();
            buku = new Buku();
            buku.setId(Integer.parseInt(model.getValueAt(i, 0).toString()));
            buku.setNama_buku(model.getValueAt(i, 1).toString());
            buku.setNama_penerbit(model.getValueAt(i, 2).toString());
            buku.setTahun_terbit(Integer.parseInt(model.getValueAt(i, 3).toString()));
            BukuTambah bukuTambahFrame = new BukuTambah(buku);
            bukuTambahFrame.setVisible(true);
        }else{
            JOptionPane.showMessageDialog(null, "Pilih data yang ingin diubah");
        }
    }//GEN-LAST:event_bUbahActionPerformed

