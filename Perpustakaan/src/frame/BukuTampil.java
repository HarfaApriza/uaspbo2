 public ArrayList<Buku> getBukuList(String keyword){
     ArrayList<Buku> bukuList = new ArrayList<Buku>();
     Koneksi koneksi = new Koneksi();
     Connection connection = koneksi.getConnection();
     
     String query = "SELECT * FROM buku "+keyword;
     Statement st;
     ResultSet rs;
     
     try{
         st = connection.createStatement();
         rs = st.executeQuery(query);
         while(rs.next()){      
             buku = new Buku(
                    rs.getInt("id"),
                    rs.getString("nama_buku"),
                    rs.getString("nama_penerbit"),
                    rs.getInt("tahun_terbit"));
             bukuList.add(buku);
         }
     } catch (SQLException | NullPointerException ex){
         System.err.println("Koneksi Null Gagal");
     }
     return bukuList;
 }
 
 public void selectBuku(String keyword){
    ArrayList<Buku> list = getBukuList(keyword);
    DefaultTableModel model = (DefaultTableModel)tBuku.getModel();
    Object[] row = new Object[4];
    
    for (int i = 0; i < list.size(); i++){
        row[0] = list.get(i).getId();
        row[1] = list.get(i).getNama_buku();
        row[2] = list.get(i).getNama_penerbit();
        row[3] = list.get(i).getTahun_terbit();

        
        model.addRow(row);

    }
}
 
 public final void resetTable(String keyword){
     DefaultTableModel model = (DefaultTableModel)tBuku.getModel();
     model.setRowCount(0);
     selectBuku(keyword);
 }
